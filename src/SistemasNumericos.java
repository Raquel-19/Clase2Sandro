public class SistemasNumericos {
    public static void main(String[] args) {

        Integer numero1 = 101;

        System.out.println("El siguiente numero en binario es: "+Integer.toBinaryString(numero1));
        System.out.println("El siguiente numero es en octal: "+Integer.toOctalString(numero1));
        System.out.println("El siguiente numero es en hexadecimal: "+Integer.toHexString(numero1));

    }
}
